section("Bug 2 — Rails-prior table is keyed on the canonical Ruby dirs")

from dataclasses import dataclass, field as dc_field  # noqa: E402


@dataclass
class _FakeKey:
    path_pattern_bucket: str = ""
    default_export_kind: str | None = None
    top_level_node_kinds: tuple[str, ...] = ()
    jsx_present: bool = False


@dataclass
class _FakeMember:
    path: Path


@dataclass
class _FakeCluster:
    key: _FakeKey
    members: list[_FakeMember] = dc_field(default_factory=list)
    cluster_id: str = "f00dcafef00dcafe"


def cluster(
    *,
    bucket: str,
    members: list[str],
    default_export: str | None = "ClassNode",
    top_level_kinds: tuple[str, ...] = ("ClassNode",),
    jsx: bool = False,
) -> _FakeCluster:
    return _FakeCluster(
        key=_FakeKey(
            path_pattern_bucket=bucket,
            default_export_kind=default_export,
            top_level_node_kinds=top_level_kinds,
            jsx_present=jsx,
        ),
        members=[_FakeMember(path=Path(p)) for p in members],
    )


# Run the heuristic over each Rails-conventional archetype directory and
# verify the priors emit the canonical name. The bucket passed in is the
# v5 signature bucket — the heuristic relies on the member paths, not the
# bucket, for Rails detection (since v5 collapses inner segments).
RAILS_CASES = [
    # (display_label, bucket, members, expected_name)
    ("app/models/ → model", "app/models", ["app/models/user.rb", "app/models/order.rb"], "model"),
    ("app/services/ → service", "app/services", ["app/services/users/create.rb", "app/services/orders/update.rb"], "service"),
    ("app/jobs/<*>_job.rb → job", "app/jobs", ["app/jobs/email_job.rb", "app/jobs/cleanup_job.rb"], "job"),
    ("app/mailers/<*>_mailer.rb → mailer", "app/mailers", ["app/mailers/user_mailer.rb", "app/mailers/order_mailer.rb"], "mailer"),
    ("app/helpers/<*>_helper.rb → helper", "app/helpers", ["app/helpers/application_helper.rb", "app/helpers/users_helper.rb"], "helper"),
    ("db/migrate/ → migration", "db/migrate", ["db/migrate/20240101_create_users.rb", "db/migrate/20240102_add_email.rb"], "migration"),
    ("app/controllers/concerns/ → controller-concern", "app/controllers/concerns", ["app/controllers/concerns/authenticatable.rb", "app/controllers/concerns/loggable.rb"], "controller-concern"),
    ("app/policies/ → policy", "app/policies", ["app/policies/user_policy.rb", "app/policies/order_policy.rb"], "policy"),
    ("app/serializers/ → serializer", "app/serializers", ["app/serializers/user_serializer.rb", "app/serializers/order_serializer.rb"], "serializer"),
    ("app/presenters/ → presenter", "app/presenters", ["app/presenters/user_presenter.rb", "app/presenters/order_presenter.rb"], "presenter"),
    ("app/workers/ → worker", "app/workers", ["app/workers/sidekiq_worker.rb", "app/workers/cleanup_worker.rb"], "worker"),
    ("app/views/ → view", "app/views", ["app/views/users/index.html.erb.rb", "app/views/orders/show.html.erb.rb"], "view"),
    ("app/controllers/ → controller", "app/controllers", ["app/controllers/users_controller.rb", "app/controllers/orders_controller.rb"], "controller"),
]
for label, bucket_, members_, expected in RAILS_CASES:
    name = propose_archetype_name(
        cluster(bucket=bucket_, members=members_),
        existing_names=set(),
    )
    t(label, name == expected, f"got {name!r}")


# A non-Ruby cluster MUST NOT engage the Rails-prior path. The TS-side
# token heuristic may still produce a familiar name for a TS file under
# `app/models/` (back-compat), but it never goes through _rails_prior_match.
# Verify the prior-match function itself short-circuits on a non-Ruby
# cluster — that's the contract the brief specifies.
match = _rails_prior_match(["app/models/types.ts", "app/models/schemas.ts"])
# _rails_prior_match doesn't itself check language (it's the caller's job),
# so it WILL match on directory-chain alone. The language gate lives in
# _is_ruby_cluster, which is called BEFORE _rails_prior_match in
# _base_name_for. Verify both halves of the gate.
t(
    "_is_ruby_cluster blocks TS clusters from the Rails-prior path",
    _is_ruby_cluster(["app/models/types.ts", "app/models/schemas.ts"]) is False,
)
# _rails_prior_match, when called on Ruby-shaped paths, returns the
# matching name — verify the function works in isolation too.
match_ruby = _rails_prior_match(["app/models/user.rb", "app/models/order.rb"])
t(
    "_rails_prior_match returns 'model' on Ruby members",
    match_ruby == "model",
    f"got {match_ruby!r}",
)


# _is_ruby_cluster is a small but load-bearing helper; sanity-check it.
t(
    "_is_ruby_cluster True for .rb members",
    _is_ruby_cluster(["app/services/foo.rb", "app/services/bar.rb"]) is True,
)
t(
    "_is_ruby_cluster False for .ts members",
    _is_ruby_cluster(["app/services/foo.ts"]) is False,
)
t(
    "_is_ruby_cluster False for empty input",
    _is_ruby_cluster([]) is False,
)

# Filename-suffix discrimination: a _job.rb under a non-jobs dir doesn't
# auto-become a job; the directory chain has the final say.
match = _rails_prior_match(["lib/random/email_job.rb", "lib/random/cleanup_job.rb"])
t(
    "_job.rb outside app/jobs/ does NOT match the job prior",
    match is None,
    f"got {match!r}",
)


