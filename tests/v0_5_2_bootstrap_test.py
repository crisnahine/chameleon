section("Bug 1 — sibling files survive atomic_profile_commit")

# Set up an existing .chameleon/ directory with both protocol files and the
# user-committed sibling files that should survive a re-bootstrap.
bug1_root = Path(tempfile.mkdtemp(prefix="chameleon_v052_bug1_"))
profile_dir = bug1_root / ".chameleon"
profile_dir.mkdir()

# Protocol-owned files (will be re-emitted by the writer).
(profile_dir / "COMMITTED").write_text("committed-at=1.0\npid=1\n")
(profile_dir / "profile.json").write_text(json.dumps({"schema_version": 7}))
(profile_dir / "idioms.md").write_text("# idioms\n## active\noriginal idiom\n")

# User-authored siblings — these MUST survive.
(profile_dir / ".skip").write_text("team opt-out — chameleon disabled\n")
(profile_dir / ".gitignore").write_text(".tmp/\n*.bak\n")
(profile_dir / ".editorconfig").write_text("[*]\nindent_size = 2\n")
(profile_dir / ".notes").write_text("hand-written team notes about chameleon\n")

# Write through atomic_profile_commit; only protocol files are emitted.
with atomic_profile_commit(profile_dir) as txn_dir:
    (txn_dir / "profile.json").write_text(json.dumps({"schema_version": 7}))
    (txn_dir / "archetypes.json").write_text(json.dumps({"archetypes": {}}))
    (txn_dir / "canonicals.json").write_text(json.dumps({"canonicals": {}}))
    (txn_dir / "rules.json").write_text(json.dumps({"rules": {}}))
    (txn_dir / "idioms.md").write_text("# idioms\n## active\nNEW idiom\n")
    (txn_dir / "profile.summary.md").write_text("# summary\n")

# Verify siblings survived.
t(
    ".skip survives atomic_profile_commit",
    (profile_dir / ".skip").is_file(),
    f"contents={(profile_dir / '.skip').read_text() if (profile_dir / '.skip').is_file() else 'MISSING'!r}",
)
t(
    ".skip contents are byte-identical to the original",
    (profile_dir / ".skip").read_text() == "team opt-out — chameleon disabled\n",
)
t(
    ".gitignore survives atomic_profile_commit",
    (profile_dir / ".gitignore").is_file(),
)
t(
    ".gitignore contents preserved",
    (profile_dir / ".gitignore").read_text() == ".tmp/\n*.bak\n",
)
t(
    ".editorconfig survives atomic_profile_commit",
    (profile_dir / ".editorconfig").is_file(),
)
t(
    "arbitrary `.notes` sibling survives",
    (profile_dir / ".notes").is_file()
    and "hand-written team notes" in (profile_dir / ".notes").read_text(),
)

# Verify the writer's new idioms.md beat the sibling-preservation path (the
# protocol file in txn_dir wins).
t(
    "writer's new idioms.md replaces the old one (txn wins over sibling)",
    "NEW idiom" in (profile_dir / "idioms.md").read_text()
    and "original idiom" not in (profile_dir / "idioms.md").read_text(),
)

# Sanity: the protocol-file set is exhaustive of what the writer emits.
expected_protocol = {
    "COMMITTED",
    "profile.json",
    "archetypes.json",
    "canonicals.json",
    "rules.json",
    "idioms.md",
    "profile.summary.md",
    "renames.json",
}
t(
    "protocol-file set matches the writer's emit list",
    _PROTOCOL_FILES == expected_protocol,
    f"got {_PROTOCOL_FILES!r}",
)
t(
    "COMMITTED constant matches the protocol set entry",
    COMMITTED_SENTINEL in _PROTOCOL_FILES,
)

# Sanity: COMMITTED sentinel is present (atomic_profile_commit always
# writes it last).
t(
    "COMMITTED sentinel written by the new txn",
    (profile_dir / "COMMITTED").is_file(),
)

shutil.rmtree(bug1_root, ignore_errors=True)


