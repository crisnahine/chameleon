section("Bug 3 — _displayed_paths_pattern repairs collapsed Rails buckets")

# The canonical failure mode from the maybe dogfood:
#   bucket = "app/rule/action_executor"
#   witness = "app/models/rule/action_executor/auto_categorize.rb"
#   expected display = "app/models/action_executor"
display = _displayed_paths_pattern(
    "app/rule/action_executor",
    "app/models/rule/action_executor/auto_categorize.rb",
)
t(
    "maybe-style buggy bucket → app/models/action_executor",
    display == "app/models/action_executor",
    f"got {display!r}",
)

# Deep namespace under app/controllers/ — the collapsed bucket loses
# `controllers/`; display must restore it.
display = _displayed_paths_pattern(
    "app/admin/dashboards",
    "app/controllers/admin/dashboards/foo.rb",
)
t(
    "app/controllers/<deep> witness → app/controllers/dashboards",
    display == "app/controllers/dashboards",
    f"got {display!r}",
)

# Bucket already contains the load-bearing segment — no change.
display = _displayed_paths_pattern(
    "app/models",
    "app/models/user.rb",
)
t(
    "bucket already correct (app/models) → unchanged",
    display == "app/models",
    f"got {display!r}",
)

# Short witness path (<4 parts) is unaffected — the v5 bucket can't have
# collapsed anything load-bearing.
display = _displayed_paths_pattern(
    "src/components",
    "src/components/Button.tsx",
)
t(
    "short non-Rails witness → bucket unchanged",
    display == "src/components",
    f"got {display!r}",
)

# Top-level segment NOT app/ — leave it alone (we don't want to over-
# correct on TS/Next.js layouts).
display = _displayed_paths_pattern(
    "src/foo/bar",
    "src/components/foo/bar/Card.tsx",
)
t(
    "non-app/ top-level → bucket unchanged",
    display == "src/foo/bar",
    f"got {display!r}",
)

# Empty witness (defensive) → fall back to bucket.
display = _displayed_paths_pattern("app/v1/users", "")
t(
    "empty witness → bucket unchanged",
    display == "app/v1/users",
)

# v0.5.2 (Bug 3): paths_pattern is byte-identical to the cluster bucket
# so runtime lookup (path_pattern_bucket_for == archetype.paths_pattern)
# still matches; the display lives separately.
# Verify that with a quick end-to-end probe.
section("Bug 3 — paths_pattern stays bucket-equivalent (lookup-compatible)")

bug3_root = Path(tempfile.mkdtemp(prefix="chameleon_v052_bug3_"))
# Hand-craft a Rails layout deep enough to trigger collapse.
(bug3_root / "Gemfile").write_text("source 'https://rubygems.org'\n")
(bug3_root / "config").mkdir()
(bug3_root / "config" / "application.rb").write_text("# Rails app\n")
# Conventional models/services dirs.
for rel in [
    "app/models/rule/action_executor/auto_categorize.rb",
    "app/models/rule/action_executor/manual_review.rb",
    "app/models/rule/action_executor/notify_owner.rb",
    "app/models/rule/action_executor/escalate.rb",
    "app/models/rule/action_executor/dismiss.rb",
    "app/services/users/create.rb",
    "app/services/users/update.rb",
    "app/services/users/destroy.rb",
    "app/services/users/list.rb",
    "app/services/users/show.rb",
]:
    fp = bug3_root / rel
    fp.parent.mkdir(parents=True, exist_ok=True)
    # Minimal valid Ruby class so prism_dump can parse it.
    fp.write_text(f"class {Path(rel).stem.title().replace('_', '')}\nend\n")

report = _orchestrator_bootstrap(bug3_root.resolve())
t(
    "Bug 3 synthetic Rails bootstrap succeeded",
    report.status == "success",
    f"got {report.status} ({report.error})",
)
# Inspect the emitted archetypes.json.
arch_path = bug3_root / ".chameleon" / "archetypes.json"
if arch_path.is_file():
    arch = json.loads(arch_path.read_text())
    has_display = any(
        "paths_pattern_display" in a for a in arch.get("archetypes", {}).values()
    )
    t(
        "at least one archetype has paths_pattern_display",
        has_display,
        f"archetypes={list(arch.get('archetypes', {}).keys())[:5]}...",
    )
    # The cluster bucket and the display string MAY differ — but never
    # equal each other when the display repair actually fired. Verify
    # that paths_pattern is bucket-shaped (no Rails repair) so runtime
    # lookup still keys correctly.
    bucket_matches_signature = True
    from chameleon_mcp.signatures import path_pattern_bucket_for
    canonicals_path = bug3_root / ".chameleon" / "canonicals.json"
    canonicals = json.loads(canonicals_path.read_text()).get("canonicals", {})
    for name, a in arch.get("archetypes", {}).items():
        witness_rels = canonicals.get(name) or []
        if not witness_rels:
            continue
        witness_rel = witness_rels[0]["witness"]["path"]
        expected_bucket = path_pattern_bucket_for(witness_rel)
        # Some signature versions append :ext; allow either form.
        if a["paths_pattern"] not in {expected_bucket, f"{expected_bucket}:rb"}:
            bucket_matches_signature = False
            print(f"    archetype {name}: stored={a['paths_pattern']!r}, computed={expected_bucket!r}")
            break
    t(
        "stored paths_pattern matches path_pattern_bucket_for(witness)",
        bucket_matches_signature,
    )
else:
    t("Bug 3 synthetic archetypes.json emitted", False, f"path={arch_path}")

shutil.rmtree(bug3_root, ignore_errors=True)


