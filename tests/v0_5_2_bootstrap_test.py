"""Regression tests for the four v0.5.2 bootstrap-pipeline bugs.

Bug 1 — atomic_profile_commit clobbers `.chameleon/.skip` + sibling files
    The transaction's dir-rename replaces the entire `.chameleon/` tree on
    every bootstrap. Committed sibling files (`.skip`, `.gitignore`,
    `.editorconfig`, hand-authored `.notes`) silently disappear on each
    refresh — defeating `.skip` as a team-shared opt-out. v0.5.2 copies
    non-protocol siblings into the txn_dir BEFORE the rename so they
    survive. Protocol files (profile.json, idioms.md, …) the writer
    emitted always win over a sibling-copy of the same name.

Bug 2 — Rails-aware naming priors in naming.py
    forem reported 5/7 archetypes as `cluster-<hex>` fallbacks even though
    the codebase had clear Rails patterns. v0.5.2 adds a Rails-prior table
    (`app/models/`, `app/services/`, `app/jobs/`, `app/mailers/`,
    `app/helpers/`, `app/policies/`, `app/serializers/`,
    `app/presenters/`, `app/workers/`, `app/views/`, `db/migrate/`,
    `app/controllers/concerns/`, …) that fires only when the cluster's
    canonical witness is a `.rb` file. Filename-suffix discriminators
    (`_job.rb`, `_mailer.rb`, `_helper.rb`) anchor against rogue files.

Bug 3 — `paths_pattern` drops `models/` prefix on some Rails archetypes
    `path_pattern_bucket_for("app/models/rule/action_executor/foo.rb")`
    yields `app/rule/action_executor` — the load-bearing `models/`
    segment is missing. Touching the bucket key would break runtime
    archetype lookup (it's the same string both writers use), so v0.5.2
    keeps `paths_pattern` byte-identical to the bucket and adds a
    sibling `paths_pattern_display` field carrying the witness-honest
    form. profile.summary.md prefers the display field so reviewers
    aren't misled.

Bug 4 — `db/schema.rb` always reported as "added" on partial-refresh
    Discovery's pre-bootstrap walk picked up `db/schema.rb` because the
    `.rb` glob matched, but the clustering layer dropped it (single-
    member generic cluster). Every refresh saw `db/schema.rb` in the
    candidate list but not in `prev_state`, so it counted as "added",
    pushed change-count > 0, and forced a full bootstrap. v0.5.2 adds
    `db/schema.rb` and `db/structure.sql` to the discovery exclusion
    set since they're Rails-autogenerated and clustering them is
    meaningless.

Run:
    cd mcp && PYTHONPATH=.:../tests .venv/bin/python ../tests/v0_5_2_bootstrap_test.py
"""

